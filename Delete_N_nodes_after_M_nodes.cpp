/*
delete n nodes after m nodes
  The input list will have at least one element  
  Node is defined as 

struct Node
{
    int data;
    struct Node *next;
    
    Node(int x){
        data = x;
        next = NULL;
    }
    
};

*/
class Solution
{
    public:
    void linkdelete(struct Node  *head, int M, int N)
    {
        Node* curr=head;
        if(curr==NULL)
        return ;
        while(curr){
        int c=1;
        while(c<M && curr)
        {
            curr=curr->next;
            c++;
        }
        Node* temp=NULL;
        if(curr!=NULL)
        {
            temp=curr->next;
            int i=0;
            while(i<N && temp)
            {
                temp=temp->next;
                i++;
            }
        }
        if(curr)
        {
            curr->next=temp;
            curr=curr->next;
            
        }
    }
        
    }
};



